#include "pasko_runtime.h"

#include <math.h>

double __pasko_arctan_f64(double x) { return atan(x); }

double __pasko_cos_f64(double x) { return cos(x); }

double __pasko_exp_f64(double x) { return exp(x); }

double __pasko_ln_f64(double x) { return log(x); }

double __pasko_sin_f64(double x) { return sin(x); }
